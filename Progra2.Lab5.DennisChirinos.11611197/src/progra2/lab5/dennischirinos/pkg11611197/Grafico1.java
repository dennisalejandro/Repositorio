    private void JB_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_AgregarActionPerformed
        String nombre = TF_Nombre.getText();
        int Edad = Integer.parseInt(TF_Edad.getText());
        int Id = Integer.parseInt(TF_ID.getText());
        int IdBoleto = Integer.parseInt(TF_IdBoleto.getText());
        String Departamento = CB_Departamento.getSelectedItem().toString();
        double Dinero = Double.parseDouble(TF_Dinero.getText());
        Secundario = TF_Secundario.getText();
        Persona e = new Persona(nombre, Edad, Id, IdBoleto, Departamento, Dinero, Trabajo, Secundario);
        ListaPersonas.add(e);
        TF_Nombre.setText("");
        TF_Edad.setText("");
        TF_ID.setText("");
        TF_IdBoleto.setText("");
        TF_Dinero.setText("");
    }//GEN-LAST:event_JB_AgregarActionPerformed

