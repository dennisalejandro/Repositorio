    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nombre");
        model.addColumn("Edad");
        model.addColumn("ID");
        model.addColumn("IdBoleto");
        model.addColumn("Departamento");
        model.addColumn("Dinero");
        model.addColumn("Trabajo");
        model.addColumn("Empleo, Carrera, Partidos");
        if (ListaPersonas.size() >= 0) {
            Object[] o = new Object[model.getColumnCount()];
            for (int i = 0; i < ListaPersonas.size(); i++) {
                o[0] = ListaPersonas.get(i).getNombre();
                o[1] = ListaPersonas.get(i).getEdad();
                o[2] = ListaPersonas.get(i).getID();
                o[3] = ListaPersonas.get(i).getIdBoleto();
                o[4] = ListaPersonas.get(i).getDepartamento();
                o[5] = ListaPersonas.get(i).getDinero();
                o[6] = ListaPersonas.get(i).getTrabajo();
                o[7] = ListaPersonas.get(i).getSecundario();
                model.addRow(o);
            }
        }
        JT_listar.setModel(model);
        CB_Eliminar.removeAllItems();
        for (int i = 0;i<ListaPersonas.size();i++) {
        CB_Eliminar.addItem(ListaPersonas.get(i).getNombre());
        }
        CB_Modificar.removeAllItems();
        for (int i = 0;i<ListaPersonas.size();i++) {
        CB_Modificar.addItem(ListaPersonas.get(i).getNombre());
        }
        
        
        

    }//GEN-LAST:event_jTabbedPane1StateChanged

